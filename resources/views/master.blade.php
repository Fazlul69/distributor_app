  <script>
	  function calculfac() {
      $('.tr').each(function(){
        var price= $('#company_price', this).val();
        var dis_price= $('#discount_price', this).val();
        var quantity= $('#quantity', this).val();
        if(dis_price == 0){
          var mult = quantity * price;
        }
        else{
          var mult = quantity * dis_price;
        }
        
        var result = mult.toFixed(2);
        $('#total', this).val(result);
      }); 
	  }
  </script>
